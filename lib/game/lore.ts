/** 階層帯（軸語彙・イベント設計の基準。UI からも参照可） */
export function tierForFloor(floor: number): "shallow" | "mid" | "deep" {
  if (floor <= 3) return "shallow";
  if (floor <= 6) return "mid";
  return "deep";
}

