import type { GameState } from "./types";
import {
  LORE_DESCENDER_ABYSS,
  LORE_DESCENDER_DEEP,
  LORE_DESCENDER_MID,
  LORE_DESCENDER_SHALLOW,
  tierForFloor,
} from "./lore";

/** 何も起きない系の短文（階層の空気感＋降り手社会の軸語彙） */
export function flavorQuiet(state: GameState): string[] {
  const t = tierForFloor(state.floor);
  const desc =
    t === "shallow"
      ? LORE_DESCENDER_SHALLOW
      : t === "mid"
        ? LORE_DESCENDER_MID
        : LORE_DESCENDER_DEEP;
  const common = [
    "風の音だけが聞こえる。",
    "足元に小石が転がっている。",
    "何も起きなかった。",
    "しばらく立ち止まった。",
    "小さな虫が這っていった。",
  ];
  if (t === "shallow") {
    return [
      ...common,
      "入口の空気が、まだ肌に残っている。",
      "苔の匂い。外の森を思い出す。",
      "天井から粒が落ちた。土か、灰か。",
      "誰かが燭台を置いていった跡がある。",
      "階段の向こうに、わずかな光が差している。",
      "落ちた「綴り」の欠片を踏んだ。誰かの魔法の残骸だ。",
      "燈台看守の印らしい刻み。もう誰もいない。",
      ...desc,
    ];
  }
  if (t === "mid") {
    return [
      ...common,
      "壁の溝に、古い蝋が詰まっている。",
      "遠くで、かすかに鎖が鳴る。風だろうか。",
      "息が白くなった。温度が下がっている。",
      "床の線が、途切れ途切れに続いている。道標の名残か。",
      "名前のない記号が並んでいる。読めば、何かが欠ける気がした。",
      "胸の奥がざわつく。まだ深い。",
      "盟約書の写しが散らばっている。墨だけが生きている。",
      "「降り手」と呼ばれた者たちの名が、壁に削られている。",
      "綴りを集める習慣は、ここでは生存術だったらしい。",
      "層底へ続く道は、誰かが意図的に短くしたようにも見える。",
      ...desc,
    ];
  }
  return [
    ...common,
    "石の脈が、血管のように見えた。目を逸らした。",
    "暗闇が重い。光を求める本能が疼く。",
    "足音が二つある気がした。振り返っても、誰もいない。",
    "底へ向かうほど、言葉が少なくなる。",
    "かつてここを通った者の残り香が、かすかにする。",
    "主の気配に近づいている——そんな錯覚がある。",
    "層底の主は、物語ではいつも同じ姿で語られる。",
    "燈が一つ、また一つと消えていく。見ないふりをした。",
    "盟約の外では、死も契約も意味を失うと古い文にあった。",
    "深い階ほど、壁の文字が「君」へ語りかけてくる気がする。",
    ...desc,
  ];
}

/** 小イベント（ダメージ・取得なしの描写） */
export function flavorAmbientDetail(state: GameState): string[] {
  const t = tierForFloor(state.floor);
  const desc =
    t === "shallow"
      ? LORE_DESCENDER_SHALLOW
      : t === "mid"
        ? LORE_DESCENDER_MID
        : LORE_DESCENDER_DEEP;
  const base = [
    "壁に文字がある。読めない。",
    "冷たい風が抜けた。",
    "水滴が落ちた。",
    "蝋が溶けかけている。",
    "誰かの足跡。古い。",
    "釘が刺さっている。触らなかった。",
    "薄い布が床に落ちていた。",
    "かすかにカビの匂い。",
    "遠くで水が滴る音がした。",
  ];
  if (t === "shallow") {
    return [
      ...base,
      "指輪が転がっていた。拾わなかった。",
      "木片に刻まれた矢印。意味は分からない。",
      "小さな祭壇。花は枯れ、石だけが残っている。",
      "折れた燈芯。まだ油の匂いがする。",
      "「下へ」だけが読める標。筆跡は三つ、重なっている。",
      ...desc,
    ];
  }
  if (t === "mid") {
    return [
      ...base,
      "折れた槍の穂先。錆が舌のように広がっている。",
      "壁に爪痕。人間のものではなさそうだ。",
      "封蝋の残骸。中身はもうない。",
      "焼け焦げた綴りの束。ページは空だ。",
      "盟約の印が床に焼き付いている。踏まないようにした。",
      "古い歌の一節が頭に浮かんだ。誰が教えたのか思い出せない。",
      ...desc,
    ];
  }
  return [
    ...base,
    "黒い染みが石に沁みている。拭っても消えない。",
    "耳元で囁かれた気がした。言葉はなかった。",
    "床に円環の焦げ跡。儀式の跡だろうか。",
    "層底の主を名指しで呼ぶと、どうなるか——誰も書いていない。",
    "深層の燈は、外の星とは逆方向に動いているという。",
    "欠けた杯に、まだ「降り」の酒の匂いが残っていた。",
    ...desc,
  ];
}

/** ボス撃破後の底の散策 */
export function flavorAbyssCalm(): string[] {
  return [
    "静かだ。",
    "風が止んでいる。",
    "もう敵はいない。",
    "底に着いた気がする。",
    "帰路は長く感じる。",
    "胸の奥がまだ熱い。",
    "主の声だけが、まだ鼓膜に残っている。",
    "石の冷たさが、いまは安らかだ。",
    "ここから上がれば、語れることが増える。",
    "盟約は破れた。代わりに、あなただけが覚えている。",
    "層底はもう「場所」ではなく、あなたの中の一語になった。",
    "燈はすべて消えた。それでも道は分かる。",
    ...LORE_DESCENDER_ABYSS,
  ];
}
